package Main;

/**
 * Represents the different states of the game.
 * <p>
 * The {@code GameState} enum is used to track the various states of the game,
 * including different screens and stages within the game. Each constant
 * represents a specific state or phase, such as the main menu, gameplay, a new
 * game, and more.
 * </p>
 *
 */
public enum GameState {
    MENU,
    PARENTLOGIN,
    NEWGAME,
    INSTRUCTIONS,
    INVENTORY,
    PARENTPLAYING,
    PLAYING,
    WALKING,
    VETSCREEN,
    GAMEOVER,
    RESUME,
    PLAYINGINS,
}
